    private void cargarDatos() {
        try {
            RegistroDeAcceso reg = new RegistroDeAcceso();
            ArrayList registros = reg.mostrarAccesos();
            
            DefaultTableModel modelo = (DefaultTableModel)tabla_reg.getModel();
            modelo.setRowCount(0);
            
            int i = 0;
            while (i<registros.size()){
                RegistroAcceso r = (RegistroAcceso)registros.get(i);
                String [] fila = {r.getSo(), r.getNav(), r.getIp(), r.getUrl(), r.getFecha()};
                modelo.addRow(fila);
                i++;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AccesoSitio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

